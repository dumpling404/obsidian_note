def move_file(src: Path, dst: Path):
    if not src.exists():
        print(f"  ⚠️  不存在（跳过）：{src.name}")
        return False
    dst.parent.mkdir(parents=True, exist_ok=True)
    shutil.move(str(src), str(dst))
    print(f"  ✅ 移动：{src.name}")
    return True


def remove_empty_dirs(root: Path):
    for dirpath in sorted(root.rglob("*"), reverse=True):
        if dirpath.is_dir():
            try:
                dirpath.rmdir()
                print(f"  🗑️  删除空文件夹：{dirpath.relative_to(root)}")
            except OSError:
                pass  # 非空，跳过


