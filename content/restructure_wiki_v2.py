    def show_tree(path: Path, prefix="", skip=None):
        skip = skip or []
        items = sorted(
            [x for x in path.iterdir() if x.name not in skip and not x.name.startswith(".")],
            key=lambda x: (x.is_file(), x.name)
        )
        for i, item in enumerate(items):
            connector = "└── " if i == len(items) - 1 else "├── "
            print(prefix + connector + item.name)
            if item.is_dir():
                extension = "    " if i == len(items) - 1 else "│   "
                show_tree(item, prefix + extension, skip=skip)

